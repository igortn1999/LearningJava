package Sec3_fundamentos;

public class ComparacaoStrings {
	
	public static void main(String[] args) {
		
		System.out.println("2"=="2");
		
		String s= new String("2");
		
		System.out.println("2"==s);//não é a melhor forma de comparação 
		
		System.out.println("2".equals(s));//melhor forma de comparação
		
		s="    2    ";
		System.out.println("2".equals(s.trim()));//trim remove os espaços e tabs 
		
	}

}
